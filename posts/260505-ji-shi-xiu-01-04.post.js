赤红。沈惊蛰的瞳孔微微收缩，他见过这种颜色。十年前，在魔界的最后记忆里，有一柄剑，剑身上缠绕着同样的赤红色雷光。那是苏长明师祖的剑，也是他幼年记忆中最温暖也最恐惧的存在——温暖是因为那柄剑曾为他挡住魔界的黑暗，恐惧是因为剑身上的雷光曾在他幼小的心灵中留下不可磨灭的印记。难道雷池深处的那点赤光，和苏长明师祖的剑有关？他正想再仔细探查，忽然，天色暗了下来。不是云层遮住了太阳，而是雷池上方的天空忽然被一种诡异的暗红色笼罩，像是有人在天穹上拉开了一道巨大的伤口。 

